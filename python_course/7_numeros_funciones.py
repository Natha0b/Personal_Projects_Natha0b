import math

print(round(1.3)) #Devuelve el numero al que tenga mas cercano en este caso el 1
print(abs(55)) #Devuelve el valor adsoluto del numero el cual le pasemos quitando los signos y dejandolo siempre como un numero positivo


print(math.ceil(1.1)) #Devuelve el numero superior mas cercano.
print(math.floor(1.9999)) #Devuelve el numero inferior mas cercano.
print(math.isnan(23)) #Evalua que lo que le estemos pasando realmente sea un numero
print(math.pow(10, 3)) #Nos permite elevar un numero a la potencia de lo que necesitemos
print(math.sqrt(9)) #Devuelve la raiz cuadrada del numero que le pasemos.
